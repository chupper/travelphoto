CREATE TABLE gallery(
    ID SERIAL PRIMARY KEY,
    NAME TEXT NOT NULL,
    DESCRIPTION TEXT NOT NULL,
    SHOW BOOLEAN NOT NULL,
    RANK INT
);

CREATE TABLE photo(
    ID SERIAL PRIMARY KEY,
    GALLERYID INT NOT NULL,
    NAME TEXT NOT NULL,
    DESCRIPTION TEXT,
    FILENAME TEXT,
    THUMBFILENAME TEXT,
    RANK INT,
    IMAGE BYTEA,
    THUMB BYTEA
);
